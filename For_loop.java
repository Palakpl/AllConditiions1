package B.Loops;

public class For_loop {

    public static void main(String[] args) {
        //int num;
        System.out.println("Printing 5 table:-");
        for(int i=5;i<6;i++)
        {
            for(int j=1;j<=10;j++)
            {

                System.out.println(i + "*" + j + " = " + i*j);
            }


        }


    }
}
